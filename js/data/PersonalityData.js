/**
 * 크리처 ID 등을 기반으로 5가지 성격 중 하나를 결정하여 반환
 */
export function getCreaturePersonality(creature) {
    if (creature.def.personalityType) {
        return PERSONALITY_ARCHETYPES[creature.def.personalityType] || PERSONALITY_ARCHETYPES.cheerful;
    }

    // Lore나 ID 해시값 기반으로 고정된 성격 할당
    // 간단히 ID의 마지막 글자나 길이를 이용해 결정
    const types = ['cheerful', 'shy', 'cool', 'noble', 'rough'];
    const hash = creature.def.id.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0);
    return PERSONALITY_ARCHETYPES[types[hash % types.length]];
}
